/**
 * @author Yvanne & Olivier
 * The Poste
 */
public class Poste {

	 private String title;
	 
	 private String description;
	 
	 private float value;

	public Poste(String title, String description, float value) {
		super();
		this.title = title;
		this.description = description;
		this.value = value;
	}

	public String getTitle() {
		return title;
	}

	public String getDescription() {
		return description;
	}

	public float getValue() {
		return value;
	}


	 
	 
	 
	 
	 
	
}
